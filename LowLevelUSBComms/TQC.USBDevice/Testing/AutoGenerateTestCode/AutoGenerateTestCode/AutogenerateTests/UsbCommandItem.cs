    [TestFixture]
    public class UsbCommandItem
    {
        [Test]
        public void ParseValue()
        {
            UsbCommandRequest cmd = new UsbCommandRequest("0x01*0x66*0x0A*0x0B|W0x07");
            Assert.That(cmd.CommandId, Is.EqualTo(0x01), "Command Id ");
            Assert.That(cmd.EnumerationID, Is.EqualTo(0x66), "Enumeration ");
            Assert.That(cmd.UsbCommand.ToString(), Is.EqualTo("Read Device Information"));
            Assert.That(cmd.UsbCommand.UsbEnumeration.ToString(), Is.EqualTo("DeviceType"));
            Assert.That(cmd.ResponseStatus.Value, Is.EqualTo(7));
            Assert.That(cmd.ExtraRequestInfo.Length, Is.EqualTo(2));
            Assert.That(cmd.ExtraRequestInfo[0], Is.EqualTo(0x0A));
            Assert.That(cmd.ExtraRequestInfo[1], Is.EqualTo(0x0B));
        }

        [Test]
        public void Equality()
        {
            UsbCommandRequest val1 = new UsbCommandRequest("0x01*0x66*|");
            UsbCommandRequest val2 = new UsbCommandRequest("0x01*0x66*|");

            Assert.That(val1, Is.EqualTo(val2));
        }
        [Test]
        public void NotEqual()
        {
            UsbCommandRequest val1 = new UsbCommandRequest("0x01*0x66*|");
            UsbCommandRequest val2 = new UsbCommandRequest("0x02*0x66*|");

            Assert.That(val1, Is.Not.EqualTo(val2));
        }
        [TestCase("", "")]
        [TestCase("A B", "AB")]
        [TestCase("a b", "AB")]
        [TestCase("abc d", "AbcD")]
        public void ConvertTextToDotNetName(string val1, string val2)
        {
            Assert.That(UsbCommandRequest.ConvertTextToDotNetName(val1), Is.EqualTo(val2));
        }

    }
